def penjumlahan(angka_pertama, angka_kedua):
    hasil = angka_pertama + angka_kedua
    print(f'Hasil Operasi : {hasil}')
    
def pengurangan(angka_pertama, angka_kedua):
    hasil = angka_pertama - angka_kedua
    print(f'Hasil Operasi : {hasil}')
    
def perkalian(angka_pertama, angka_kedua):
    hasil = angka_pertama * angka_kedua
    print(f'Hasil Operasi : {hasil}')